function sortDates(date1, date2) {
  let d1 = date1.split("-").map((el) => Number(el));
  let d2 = date2.split("-").map((el) => Number(el));
  if (d1[0] < d2[0]) return -1;
  else if (d1[0] > d2[0]) return 1;
  else if (d1[1] < d2[1]) return -1;
  else if (d1[1] > d2[1]) return 1;
  else if (d1[2] < d2[2]) return -1;
  else if (d1[2] > d2[2]) return 1;
  else return 0;
}

function createRows() {
  let date = document.getElementById("selected-date").value;
  let mh_arr = mh_data[date];
  document.getElementById("tbody-start").innerHTML = "";
  for (let games in mh_arr) {
    for (let game in mh_arr[games]) {
      let team1 = mh_arr[games][game]["team1"];
      let team2 = mh_arr[games][game]["team2"];
      for (let i = 0; i < 3; i++) {
        let team1class = "";
        let team2class = "";
        if (
          mh_arr[games][game]["team1_score"] >
          mh_arr[games][game]["team2_score"]
        ) {
          team1class = "matchwin";
          team2class = "matchloss";
        } else {
          team1class = "matchloss";
          team2class = "matchwin";
        }

        let player1 = Object.keys(team1)[i];
        let player2 = Object.keys(team2)[i];
        let row = document.createElement("tr");
        let content = `
        <td class='${team1class}' style='padding-left: 10px'>${names[player1]}</th>
        <td class='${team1class}' style='text-align:center'>${team1[player1].turns}</td>
        <td class='${team1class}' style='text-align:center'>${team1[player1].defense}</td>
        <td class='${team1class}' style='text-align:center'>${team1[player1].assists}</td>
        <td class='${team1class}' style='text-align:center'>${team1[player1].goals}</td>`;
        if (i == 0) {
          content += `
          <td class='${team1class}' rowspan='3' style='text-align:center; border-right: solid 2px black'><div>${mh_arr[games][game]["team1_score"]}</div></td>
          <td class='${team2class}' rowspan='3' style='text-align:center'></div>${mh_arr[games][game]["team2_score"]}</div></td>
          `;
        }
        content += `
        <td class='${team2class}' style='text-align:center'>${team2[player2].goals}</td>
        <td class='${team2class}' style='text-align:center'>${team2[player2].assists}</td>
        <td class='${team2class}' style='text-align:center'>${team2[player2].defense}</td>
        <td class='${team2class}' style='text-align:center'>${team2[player2].turns}</td>
        <td class='${team2class}' style='text-align:right; padding-right: 10px'>${names[player2]}</th>
        `;
        row.innerHTML = content;
        if (i == 2) row.style.borderBottom = "solid 2px black";
        document.getElementById("tbody-start").appendChild(row);
      }
    }
  }
}
